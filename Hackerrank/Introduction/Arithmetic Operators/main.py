
def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def multi(a,b):
    return a*b




if __name__ == '__main__':
    a = int(input())
    b = int(input())
    print(add(a,b))
    print(sub(a,b))
    print(multi(a,b))



